  print("a >= b : ${a >= b}"); // a b'ye büyük eşit mi

  print("a < b : ${a < b}"); // a b'den küçük mü
  print("a <= b : ${a <= b}"); // a b'ye küçük eşit mi

  print("a > b || x > y : ${a > b || x > y}"); // || = veya (or)
  print("a > b && x > y : ${a > b && x > y}"); // && = ve (and)

}
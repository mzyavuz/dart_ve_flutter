class Islemler {

  double sifirla(sayi) {
    return sayi=0.0;
  }

  double eksiDegeri(sayi) {
    return sayi=sayi*-1;
  }

  double yuzdesi(sayi) {
    return sayi = sayi/100;
  }

  double bolme(sayi, sayi2) {
    return sayi = sayi / sayi2;
  }

  double carpma(sayi, sayi2) {
    return sayi = sayi * sayi2;
  }

  double cikarma(sayi, sayi2) {
    return sayi = sayi - sayi2;
  }

  double toplama(sayi, sayi2) {
    return sayi = sayi + sayi2;
  }
}